<div id="about_div" class="page_div">PSDB (Plate Spotting DataBasen) är
	till för dig som håller på med platespotting. Den låter dig spara
	positionen på nummerplåtar som du senare kan leta upp när det är dax
	att spotta just det nummret. Alla plåtar du sparar är sökbara för alla
	andra vilket gör att vi på psdb alla hjälper varandra. Du vet ju själv
	hur frustrerande det kan vara när du letar efter ett nummer som du vet
	att du nästan dagligen brukade se förut men inte kommer ihåg vart. Med
	hjälp av plate spotting databasen så kan du spara positionen på plåtar
	som du vet står på samma ställe varje dag och på sätt enkelt hitta
	tillbaka till den när det är dax.</div>
